  <h1>E-Posta Etiği</h1>
  <div>
    E-Posta, internet üzerinden dünya ile ilişki kurulmasını sağlayan en büyük hizmet imkanıdır. Bu denli büyük işlevi yerine getiren hizmetin kullanılmasında, uyulması gereken bir takım kurallar vardır. Bunlar;<br />
    <br />
    <ol>
      <li>Kullanıcı kimlik bilgilerinizi (kullanıcı adınızı ve şifrenizi) kimseye vermeyin. Sistem yöneticisi herhangi bir sebepten dolayı kullanıcı bilgilerinizi sisteme erişmek için tam yetkili yapmış olabilir.</li>
      <li>Mesajlarınızı ana fikir belirterek kısa ve öz yazın.</li>
      <li>Zincir mesajlara cevap verip, zincirin uzamasına neden olmayın.</li>
      <li>Mesajınıza anlamlı bir başlık yazarak konu dışına çıkmayın.</li>
      <li>Özel ve ticari işleriniz için akademik veya resmi ağları kullanmayın.</li>
      <li>Özellikle tanımadığınız ortamlara e-posta gönderirken, mesajınızın sonuna “elektronik imza mesajınızı” ekleyin. (isminiz, pozisyonunuz, e-mail adresiniz) Bu mesajın en çok 4 satır olmasına dikkat edin. İsteğe bağlı olarak da adresiniz ve telefon numaranızı yazabilirsiniz.</li>
      <li>Sadece başlıklarda veya çok önemli noktalarda büyük harf kullanın. Başlık dışındaki büyük harflerin internette bağırmak anlamına geldiğini unutmayın.</li>
      <li>Mesajınızda dikkat çekmek istediğiniz noktaları * asteriks işareti arasına * alın.</li>
      <li>Kitap isimlerinin önüne ve arkasına _ kesme işareti koyun. (ör._Internetin Kitabı_ )</li>
      <li>Mesajlarınızda satır uzunluğu en fazla 65 – 70 karakter olmalı ve kontrol karakterleri içermemelidir.</li>
      <li>Mesajınızda tarih bilgisi yazacaksanız, ay bilgisini harfle yazın. (ör. 30 Agus. 2002)</li>
      <li>Mesajınıza yazacağınız cümleler konusunda herhangi bir yanlış anlaşılma olmaması için çok dikkatli olun.</li>
      <li>Mesajınızda kullandığınız her türlü alıntı için referans belirtin.</li>
      <li>Espri niteliğinde yazacağınız cümlelerin yanlış anlaşılmamasına dikkat edin.</li>
      <li>Mesajınızın anlaşılır olması için, mümkün olduğu kadar kısaltmalardan kaçının.</li>
      <li>Elektronik posta, insanlarla iletişim amacını taşır. Bir mail yazdığınızda göndermeden önce bir kere okuyun ve kendi kendinize “ben bu maili alsaydım ne yapardım, reaksiyonum ne olurdu?” diye sorun. Unutmayın ki, yazdığınız bir cümle öyle kalmak zorunda değil. Göndermeden önce değiştirebilir, anlamını daha iyi pekiştirebilirsiniz. Yazılı anlatımın sözlü anlatıma karşı bu üstünlüğünü kullanın.</li>
    </ol>
  </div>


  <h1>Haberleşme ve Tartışma Listeleri Etiği</h1>
  <div>
    <ol>
      <li>Yeni bir mesaj listesine üye olduğunuzda, liste konusunu ve soruları tam anlayabilmek için gelen mesajları birkaç gün denetleyip, listenin güvenilirliğini test ettikten sonra mesaj gönderin.</li>
      <li>Üyesi bulunduğunuz grubun, sık sorulan sorular kısmı varsa, sürekli gözden geçirin.</li>
      <li>Liste için belirlenen kurallara mutlaka uyun.</li>
      <li>Bazı tartışma gruplarının içerisinde, farklı ülkelerden üyeler olabileceğini göz önünde bulundurup, yerel veya kendi coğrafyanızla ilgili referanslar verdiğiniz zaman açıklayıcı olun.</li>
      <li>Kışkırtıcı ve rahatsız edici mesajlar göndermeyin. Bu tür mesajlar çok ters tepkilere yol açar ve listeye üyelik hakkınızı kaybedebilirsiniz</li>
      <li>Sorularınızı ve yorumlarınızı tartışma grubunun ilgili alanından seçin.</li>
      <li>Konu dışı gelen mesajlara cevap vermeyin.</li>
      <li>Bir haftadan uzun süren seyahatlerinizi, listserv veya tartışma grubu servislerine bildirin.</li>
      <li>Liste trafiğini artırmamak için, listeye gönderilen soruların cevabını, direkt soruyu soran kişinin e-postasına gönderin.</li>
      <li>Mesaj listesine veya tartışma gruplarına uzun mesajlar göndermeyin. Gerekirse mesajın referansı veya bir kopyasının nasıl elde edilebileceğini belirtin. Uzun mesaj gönderiminin zorunlu olduğu durumlarda bunu mesajın başında belirtin. (ör. DİKKAT: UZUN MESAJ)</li>
      <li>Size gelen mesajlara yanıt yazarken, orijinal mesajı cevabınıza eklemeyin. Günümüzde kullanılan e-posta programlarının hemen hemen tamamı orijinal mesajı da gönderdiğiniz e-posta’nın sonuna ekler.</li>
      <li>Gönderdiğiniz mesajın adresini kontrol edin. Aynı listeye üye olan arkadaşınıza özel mesaj gönderdiğinizde, bunu yanlışlıkla listeye gönderebilirsiniz.</li>
      <li>Aynı anda çoklu gruplara mesaj gönderiyorsanız, bunu özür dileyerek, mesajın başında belirtin.</li>
      <li>Birisine, ya da bir gruba/listeye bir konu hakkında soru sorduğunuz da “lütfen” demeyi unutmayın. Sizin için birisi yanıt yazmış, sorunuzla ilgilenip çareler üretmeye çalışmışsa, bir mail ile ilgili kişiye “teşekkür ederim” demeniz herhalde sizi yaralamaz. Günlük yaşamlarında son derece nazik olan pek çok insan, elektronik haberleşme ortamlarında bambaşka oluveriyorlar. Oysa, ortamlar farklı olsa, hem gerçek hayatta hem de elektronik haberleşme yaparken ilişkide bulunduklarımız “insan”.</li>
      <li>Gruba/listeye sorduğunuz sorunun cevabını, direkt olarak e-postanıza gönderilmesini isteyin ve daha sonra cevabı veya cevabın özetini listeye gönderin.</li>
      <li>Mesaj listesi sahibiyseniz, listenize kayıt yaptıran kişilerin bunu şahsen yaptıklarını anlamak üzere, listenize almadan önce, kendilerine matbu bir e-posta gönderin.</li>
    </ol>
  </div>

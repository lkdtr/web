      <li>
        LKD listeleri derneğin resmi destek araçları değildir, yalnızca LKD
        çevresindeki kişilerin gönüllü katkıları ile, diğer gönüllülerin
        sorunlarına yardımcı olmaya çalıştıkları araçlardır. Listelere
        gönderilen tüm mesajların sorumluluğu gönderen kişiye aittir, hukuksal
        olarak da derneği hiçbir biçimde bağlamaz. Özellikle sorun çözümü için
        öneri içeren mesajlar, yanıtlayan kişi dahil, hiçbir kişi veya derneği
        kötü sonuçlara karşı sorumlu kılmaz. Kullanıcı listedeki önerileri
        kendi sorumluluğu altında uyguladığını unutmamalıdır.
        <br /><br />
      </li>
      <li>
        LKD listeleri, liste üyelerinin mesajlarını otomatik (moderasyon
        yapmadan) dağıtmaktadır.  Liste yöneticilerinin mesajlarda değiştirme,
        yeniden düzenleme veya geciktirme gibi bir girişimleri kesinlikle
        yoktur. Ancak liste yöneticileri gerekli gördükleri taktirde, geçici
		olarak listeyi moderasyona alabilirler. Zaten listelere üyelik de kişilerin
		isteği ile başlamakta veya sona ermektedir.
        <br /><br />
      </li>
      <li>
        Gönderilen mesajlar sunucunun yoğunluğuna bağlı olarak, kısa sürede
        dağıtılmaktadır. Liste üyelerinin yanıtları ise, kendi yoğunluklarına
        bağlı olarak iletiyi okuyup, yanıt yazama zamanlarına bağlıdır,
        listelerdeki sorulara belli bir zamanda yanıt verileceği gibi kural
        yoktur.<br />
        <br />
        Bu nedenle bir iletinize yanıt alamaz iseniz, aynı iletiyi
        yinelemeyiniz. Yanıtı bilebilecek gönüllüler yalnızca haftasonları
        liste mesajlarına bakıyor olabilirler. Benzer bir sorun yakın zamanda
        yanıtlanmış olabilir, aşağıda adresi verilen eski liste mesajları
        içinde arama sayfasını kullanmak çok daha hızlı çözüm bulmanızı
        sağlayabilir.<br />
        <br />
        Yineleyen iletiler, başlıkta "acil", "çok önemli" gibi uyarıcı sıfatlar
        kullanılan iletiler gönüllüler tarafından pek hoş karşılanmamaktadır.
        İletinize hiç yanıt verilmez ise, kimsenin sorunun yanıtını bilemediği
        de dahil olmak üzere, bir sorun olabileceğini düşünüp, bireysel olarak
        çevrenize danışınız.<br />
        <br />
        Özellikle başlangıçta listelere soru sormadan önce aşağıdaki web
        alanlarına göz atmak yararlı olabilir. Sizin "çok basit"
        görebileceğiniz bazı sorular, listedeki gönüllüler tarafından da
        bilinmeyebilir veya gönüllülerin ilgisini çekmeyebilir.<br />
        <br />

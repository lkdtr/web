      <li>
        Zincir elektronik iletiler (chain mail) listelere yollanmamalıdır.
        <br /><br />
      </li>
      <li>
        İletinin konusu konu satırında kısaca belirtilmeli, önceki bir
        konuya yanıt olarak ileti yazılmaya başlanarak, konu satırı ile ilgisiz
        içerik gönderilmesinden kaçınılmalıdır. Üyelerin posta kutularının
        korunması amacı ile, konunun hangi listeyi ilgilendireceği düşünülerek
        ileti gönderilmelidir.
        <br /><br />
      </li>
      <li>
        Kişisel amaçlı iletişim için listeleri kullanmayınız. Kendinizin
        veya başkalarının kişisel bilgilerini (adres, cep telefonu, banka hesap
        numarası vb.) listeye iletmeyiniz. Listeye yazan kişiyi özel olarak
        yanıtlamak isteyip de, yanıtınızı yanlışlıkla listeye gönderirseniz,
        listeye hemen kısa bir özür mesajı gönderiniz.
        <br /><br />
      </li>
      <li>
        Listelere gönderilebilecek ileti boyutu 40 KB olarak
        sınırlandırılmışıtr. Daha büyük boyutlu dosyalar için, web veya ftp
        alanlarına yerleştirilmesi ve ilgili adresin liste ile paylaşılması
        önerilen yöntemdir.
        <br /><br />
      </li>
      <li>
        Liste kurallarına uymayanlar, listelerde gerekli nezaketi
        göstermeyenler, kavgaya yol açanlar, konu dışı ileti gönderenler için
        liste yönetimi, listelerden çıkarmaya varan yaptırımlar uygulayabilir.
        Bu gibi iletilere liste yönetimince özel uyarı mesajı
        gönderileceğinden, listelerde bu tür iletileri "yok farz ederek"
        davranınız, ilgili kişiye liste aracılığı ile davranışına yönelik uyarı
        iletisi göndermeyiniz (Kişisel iletilerinize kimse karışmamaktadır...).

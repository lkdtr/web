        Sisteminizde olusan bir sorun hakkında yazıp yardım isteyecekseniz,
        sisteminiz ve oluşan sorun hakkında ayrıntılı bilgi vermeye çalışın:
        "Şu dağıtım (Mandrake 10.1, Debian stable, ...) , şu yazılım ve şu
        sürümü (gcc 4.3, Apache 2.0.51, ...) , şu girdi ile şunlar oluyor" türü
        olabildiğince ayrıntı vermeye çalışın. Bu arada ilgili kayıt (log)
        dosyalarında ne yazıldığına bir bakın, ve onları da iletmeye çalışın.
        Her duruma uyan bir reçete olmasa da, ayrıntıları bilmeden de size

  <h1 class="head2">Otomatik Elektronik Posta Listesi Nasıl Çalışmaktadır?</h1>
  <br />

  <div>
    Elektronik posta  listeleri çoğunlukla özel bir posta listesi yazılımı ve bir sunucu üzerinde e-posta alabilecek şekilde ayarlanmış bir yansıtıcı adres aracılığı ile tam yada kısmen otomatikleştirilmişlerdir. Yansıtıcı adrese gönderilmiş mesajlar yazılım tarafından işlerin ve içeriklerine göre ya dahili olarak tesir edilirler ( yazılımın kendisine direktif veren komutlar içermesi halinde ) yada  bu listeye kayıt olmuş tüm e-posta adreslerine dağıtılır. Yazılıma bağlı olarak ek adreslerde komut gönderim amacıyla oluşturulabilir. Birçok elektronik posta adresi sunucuları üyelerinin (yada üye olacakların) kayıt olmak ve kayıt sildirmek, geçici olarak kendilerine mesaj gönderimini durdurmak, yada uygunluk tercihlerini değiştirmek gibi görevleri yerine getirmesi için komutlar gönderebileceği özel bir e-posta adresleri vardır. Bu komutları yollamanın genel formatı ise e-posta listesinin adının takibinde komutu içeren bir e-posta yollamaktır. Örnek olarak: kaydol listeadı yada kaydol listeadı isim soyisim. Bazı liste sunucuları kaydolma, kayıtsilme, ayarları değiştirmek vs. işlemlerini bir web sayfası üzerinden yapmalarına imkan verirler.<br />
    <br />
    Elektronik posta listesi sunucuları belirli bir posta listesi kullanıcılarına mesajları ya kendisine ulaştığı anda hemen iletmeye ayarlanmıştır yada belli bir süre sınırı içinde alınmış tüm mesajları bir düzende tek mesaj olarak birleştirip günde bir adet olarak üyelere iletmektedirler. Bazı posta listeleri ise bunu üyelerinin ferdi olarak karar verip liste sunucusunda seçmelerine izin vermektedir. (individual yada digest)<br />
    <br />
  </div>



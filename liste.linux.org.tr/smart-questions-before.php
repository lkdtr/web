      <div class="para"> Sorunuzu sorarken, yukarıda sayılanları yaptığınızı belirtin. Böylece sizin tembel teneke olmadığınız ve insanların zamanlarını boşa harcamayacağınız anlaşılsın. Daha iyisi, bunları yaparak neler öğrendiğinizi de gösterin. Cevaplardan bir şeyler öğrenebileceğini göstermiş olan kişilerin sorularına cevap vermeyi severiz. </div>
      <div class="para"> Almış olduğunuz hata iletilerindeki metni kullanarak Google'da probleminizi aratın (ayrıca Google gruplarını da aratın). Bu sizi doğruca, sorunun çözümü olan belgeye veya sorunuza yanıt verilen eposta listesi iletisine götürebilir. İşe yarar bir sonuç alamasanız bile, bir posta veya haber grubunda: "Google'da hata iletisini arattım ama işe yarar bir sonuç elde edemedim"  şeklinde bir e-posta ya da haber göndermek oldukça iyi bir artıdır. </div>
      <div class="para"> Sorunuzu hazırlayın. İyice düşünün. Acele hazırlandığı belli olan sorular acele cevaplar alırlar ya da hiç cevap alamazlar. Sorunuzu göndermeden önce sorunu çözmek için daha çok çaba göstermek ve bunu ortaya koymak gerçek yardım alma ihtimalinizi artırır. </div>
      <div class="para"> Yanlış soru sormayın. Hatalı kabullere dayalı bir soru sorduğunuzda herhangi bir üstadın size baştan savma bir cevap vermesine ve "ne aptalca bir soru" şeklinde düşünmesine sebep olursunuz.  Üstatlar bu tür cevaplarında, sizin sorununuzun çözülmesini değil, yaptığınız aptallıktan ders almanızı umarlar. </div>
      <div class="para"> Asla cevap alma hakkınız olduğunu düşünmeyin. Böyle bir hakkınız yok. Bu tür servisler için bedel ödemediğiniz sürece de olmayacak. Şayet kazanabilirseniz, bir cevap kazanacaksınız. Bunun için sağlam, ilginç, kışkırtıcı bir soru sormalı ve topluluğun bilgisine ihtiyaç duyan değil, bu bilginin artmasını sağlayan biri gibi davranmalısınız. </div>
      <div class="para"> Diğer taraftan, sorunu açıklayıcı bir hale getirmek ve çözümün üretilmesi aşamasında yardımcı olmaya niyetli olduğunuzu göstermek iyi bir başlangıç sayılır. "Birileri bir başlangıç noktası verebilir mi?", "Benim çalışmamdaki eksiklik nedir?" ve "Hangi siteyi kontrol etmeliyim?" tarzındaki sorular "lütfen bana tam süreci anlatın" şeklindeki bir sorudan daha iyidir ve cevap alma şansınız daha yüksektir. Çünkü doğru cevaba ulaşmak için çaba göstermek ve bunu belirtmek, şayet birisi size başlangıç noktasını gösterirse sonuca gidebileceğinizi göstermek her zaman daha uygundur. </div>

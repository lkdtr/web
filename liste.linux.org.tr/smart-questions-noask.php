          <dt class="question"><a name="id2" id="id2"></a><a name="id2" id="id2"></a><b>6.2. </b> <b>Y'yi yapmak için X'i nasıl kullanabilirim?</b></dt>
          <dd class="answer"><b></b>
            <div class="para">Şayet yapmak istediğiniz şey Y ise, bu sorunu çözmek için uygun olmayan bir yöntemi kullanarak, bu sorunu nasıl çözeceğinizi sormayın. Bu şekilde sorulmuş sorular, X hakkında cahil ve çözmeye çalıştığınız Y sorunu hakkında tam bir bilgiye sahip olmayan insanlar olduğunuz izlemini uyandırır.   Genelde bu tür insanlarla, sorunlarını açıklayana kadar ilgilenmemek en iyi yöntemdir.</div>
          </dd>
          <dt class="question"><a name="id3" id="id3"></a><a name="id3" id="id3"></a><b>6.3. </b> <b>Kabuk ayarlarımı nasıl yapabilirim?</b></dt>

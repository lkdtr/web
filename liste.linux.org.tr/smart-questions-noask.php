          <dt class="question"><a name="id7" id="id7"></a><a name="id7" id="id7"></a><b>6.7. </b> <b>Yazılımım çalışmıyor. Sanırım  sistemdeki X bozuk.</b></dt>
          <dd class="answer"><b></b>
            <div class="para">Yüzlerce veya binlerce kişi tarafından kullanılan sistem çağrılarındaki ve kütüphanelerindeki apaçık bir eksikliği bildiren ilk kişi olmanız muhtemel olmakla birlikte, elinizde sarsılmaz deliller olmadığı gün gibi aşikar. Olağandışı iddialar, olağandışı kanıtlar gerektirir; bu tür bir şuçlama yaparken, durumu açık seçik beyan etmeniz ve hatayı meydana getiren olayları ayrıntılı bir  şekilde belgelemeniz gerekir.</div>
          </dd>
          <dt class="question"><a name="id8" id="id8"></a><a name="id8" id="id8"></a><b>6.8. </b> <b>Linux veya X'i yüklerken sorun yaşıyorum. Yardım eder misiniz?</b></dt>

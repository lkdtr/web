          <dt class="question"><a name="id1" id="id1"></a><a name="id1" id="id1"></a><b>6.1. </b> <b>X uygulamasını veya kaynak kodunu nereden bulabilirim?</b></dt>
          <dd class="answer"><b></b>
            <div class="para">Benim bulduğum yerden, -- internette bir aramadan sonra. Kimse <a class="ulink" href="http://www.google.com.tr" target="_top">Google'ı</a> kullanmayı bilmiyor mu?</div>

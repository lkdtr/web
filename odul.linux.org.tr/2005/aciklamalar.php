LKD Yılın Penguenleri Ödülleri'nin belirlenmesinde uygulanacak yöntem aşağıdaki gibidir.
Belgeyi PDF biçeminde indirmek için <a href="indir/yilin-penguenleri-odulleri-2005.pdf">tıklayın.</a><br><br>


---------------------------------------------------------<br><br>

- Ödüller bir önceki yılda yapılan çalışmalar göz önünde bulundurularak verilir.
<br><br>
- Daha önce bir kategoride 'Yılın Pengueni' ödülünü almış bir kişi/kurum/proje tekrar aynı ödüle aday gösterilemez. Ancak farklı bir kategoride aday gösterilebilir.
<br><br>
- 01 Nisan - 23 Nisan tarihleri arasında, "Aday Gösterme" süreci gerçekleşir. Her ödül kategorisi için web sayfasında bir aday göster formu yerleştirilir. Mevcut adaylar için de, adaylar hakkında değerlendirme yapılabilmesi için insanların görüşlerini yazabileceği bir alan oluşturulur. Küfür ve hakaret içermeyen aday ile ilgili tüm görüşlerin yayınlanması esastır.
<br><br>
- Aday gösterilen bir kişi/kurum/proje, aday gösterme süreci sırasında adaylığını geri çekebilir veya kabul etmeyebilir. Böyle bir resmi istek durumunda ilgili kişi/kurum/proje tekrar eklenmemek üzere aday listesinden çıkarılır.
<br><br>
- 25 Nisan - 08 Mayıs tarihleri arasında dernek üyelerine açık bir çağrıda bulunularak, her kategoride aday olanlar arasından birer adayı seçerek oy vermeleri istenir. En çok oyu alan aday ödülü kazanırken, eşitlik durumunda en çok oyu eşit olarak alan adayların katıldığı ikinci tur oylamaya geçilir. 09 Mayıs - 15 Mayıs tarihleri arasında yapılan ikinci tur oylamada da eşitliğin yine bozulmaması durumunda ödül, ikinci turda en çok oyu eşit olarak alan adaylar arasında paylaşılır.
<br><br>
- Oylamada her kategoride adayların yanı sıra birer "Fikrim yok" ve "Ödül verilmesin" seçenekleri yerleştirilir. "Fikrim yok" oyu verenler dışında verilen toplam oy sayısının %50'sinden fazlasının "Ödül verilmesin" olması durumunda o sene ilgili kategoride ödül verilmez.
<br><br>

- Ödüller 22 Mayıs'ta, 4. Linux ve Özgür Yazılım Şenliği'nin kapanış töreni sırasında sahiplerine verilir.
<br><br>
- Ödüller 6 kategoride verilmektedir :
<br><br>
* En Çalışkan Penguen : Geçtiğimiz yıl içerisinde özgür yazılım olgusunun Türkiye'de gelişmesi ve yaygınlaşmasına kişisel olarak en çok katkıda bulunmuş olan penguen. Bu ödül kişilere verilmektedir.
<br><br>
* En İyi Kurumsal Kullanıcı : Geçtiğimiz yıl içerisinde özgür yazılımları Türkiye'de en yaygın ve verimli biçimde kendi bünyesinde kullanmış olan kurum. Bu ödül kurumlara verilmektedir. Ödüle bilgi işlem teknolojileri ile profesyonel olarak ilgilenen kurumlar aday olamaz.
<br><br>
* En Başarılı Özgür Yazılım Temelli Uygulama Projesi : Geçtiğimiz yıl içerisinde özgür yazılımlar ve özgür bir işletim sistemi kullanılarak özgün bir konuda Türkiye'de gerçekleştirilen en başarılı yapı bütünü. Bu ödül projelere verilmektedir.
<br><br>
* En Başarılı Yerelleştirici : Geçtiğimiz yıl içerisinde özgür yazılımların Türkçe dil desteğinin geliştirilmesine ve yaygınlaştırılmasına en çok kişisel olarak katkıda bulunmuş penguen. Bu ödül kişilere verilmektedir.
<br><br>
* En İyi Özgür Yazılım : Geçtiğimiz yıl içerisinde GPL (Genel Kamu Lisansı) ya da uyumlu bir lisans ile halka açık olarak Türkiye'de geliştirilmiş olan en iyi yazılım. Bu ödül projelere verilmektedir.
<br><br>
* En İyi Basılı/Görsel İçerik Çalışması : Geçtiğimiz yıl içerisinde hazırlanan ve Türkiye'de özgür yazılımların yaygınlaştırılmasına en çok katkıda bulunmuş kitap, dergi, web sitesi, radyo/televizyon programı ve benzeri çalışma. Bu ödül projelere verilmektedir.


		<title>IV. Linux ve Özgür Yazılım Şenliği Başlıyor...</title>
		<link>http://senlik.linux.org.tr/2005b/index.php?option=com_content&amp;task=view&amp;id=69ampItemid=2</link>
		<description>Linux Kullanıcıları Derneği'nin her yıl düzenlediği Linux ve Özgür Yazılım
Şenliği 19-22 Mayıs 2005 tarihleri arasında Ankara Milli Kütüphane
Konferans Salonları'nda gerçekleşecek.
&lt;br /&gt;&lt;br /&gt;
Linux ve Özgür Yazılım Şenliği; 16-19 Mayıs 2002'de Ankara Üniversitesi, 
1-4 Mayıs 2003'te Yıldız Teknik Üniversitesi ve 13-16 Mayıs 2004'te Milli
Kütüphane'de olduğu gibi bu yıl da Türkiye'deki tüm penguenlerin buluşma ve
kaynaşmasını sağlayan en büyük etkinlik olma özelliğini taşıyor. Bu yıl
dördüncüsü düzenlecek olan Linux ve Özgür Yazılım Şenliği, Linux
dünyasındaki son gelişmeleri yansıtan, hiç Linux bilmeyen ya da az bilen
kullanıcının, ev kullanıcısının, iş dünyasının ve Linux'a destek veren
firmaların buluştuğu bir ortam olmayı hedefleyen bir çizgiye sahip.
&lt;br /&gt;&lt;br /&gt;
Şenliğin açılışında, Linux Kullanıcıları Derneği Başkanı Sayın Doç. Dr.
Mustafa Akgül konuşma yapacak.
&lt;br /&gt;&lt;br /&gt;
Şenliğin sponsorları IBM Türk Ltd., Prosoft İletişim Sistemleri Ltd. Şti.
ve 2U Bilişim Teknolojileri olurken; Intel ve Elektrik Mühendisleri Odası
Ankara Şubesi de etkinliği kurumsal olarak destekliyor. Kayıt masası
sponsorluğu Parkyeri A.Ş., basın sponsorluğu PCWorld, RadyoODTÜ, KanalB,
oyun turnuvasının ödülleri ve internet kafede kullanılan bilgisayarları
Mavi Bilgisayar, kalem sponsorluğu PostgreSQL, içecek sponsorluğu Nescafe
tarafından üstlenildi.
&lt;br /&gt;&lt;br /&gt;
Şenlikte çeşitli konularda 3 paralel salonda yapılacak teknik seminerlerin
yanısıra, &quot;Yılın Penguenleri Ödülleri&quot;, ayaküstü sohbetler, farklı Linux
dağıtımlarının kurulumları, oyun turnuvaları, &quot;Getirin Bilgisayarınıza
Linux Kuralım&quot; kampanyası, internet kafede Linux'la tanışabilme, katılan
firmalarin yetkilileriyle birebir görüşme fırsatları ve daha birçok
etkinlik ziyaretçilere sunulacak.
&lt;br /&gt;&lt;br /&gt;
Şenliğimize katılım ücretsiz ve herkese açıktır. Gelişmeleri takip etmek ve
detaylı bilgi edinmek için şenlik web sitesini (&lt;a href=&quot;http://senlik.linux.org.tr&quot; target=&quot;_blank&quot;&gt;http://senlik.linux.org.tr&lt;/a&gt;)
inceleyebilirsiniz.
&lt;br /&gt;&lt;br /&gt;
Yüzlerce gönüllü penguen sayesinde bu yıl dördüncüsünü gerçeklestireceğimiz
Linux ve Özgür Yazılım Şenliği'ne katılımlarınızla bizlere manevi destek
vermiş olacaksınız.</description>
